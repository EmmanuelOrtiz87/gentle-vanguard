# create-installer.ps1
# Builds Gentle-Vanguard.exe — single NSIS installer (AES-256 encrypted, all-in-one)
# Output: dist/Gentle-Vanguard.exe — replaces legacy Gentle-Vanguard-Setup.exe and Gentle-Vanguard-Launcher.exe
# Prerequisites: NSIS 3+ installed (makensis.exe in PATH or standard location)
# Usage: pwsh -File build/create-installer.ps1 [-SkipEncrypt] [-DryRun]

param(
    [switch]$SkipEncrypt,
    [switch]$DryRun
)

$ErrorActionPreference = 'Stop'

#region RepoRoot Resolution
if ($env:GENTLE_VANGUARD_BASE_DIR) {
    $repoRoot = $env:GENTLE_VANGUARD_BASE_DIR
} else {
    $searchDir = $PSScriptRoot
    while ($searchDir -and -not (Test-Path (Join-Path $searchDir 'config\orchestrator.json'))) {
        $searchDir = Split-Path -Parent $searchDir
    }
    $repoRoot = $searchDir
}
#endregion

$buildDir = Join-Path $repoRoot 'build'
$distDir = Join-Path $repoRoot 'dist'
$protectedDir = Join-Path $buildDir 'protected'
$publicDir = Join-Path $buildDir 'public'

function Write-Step { param([string]$msg) Write-Host "[BUILD] $msg" -ForegroundColor Cyan }
function Write-OK { param([string]$msg) Write-Host "  [OK] $msg" -ForegroundColor Green }
function Write-Warn { param([string]$msg) Write-Host "  [WARN] $msg" -ForegroundColor Yellow }
function Write-Err { param([string]$msg) Write-Host "  [ERROR] $msg" -ForegroundColor Red }

#region Step 1: Encrypt if not skipped
if (-not $SkipEncrypt) {
    Write-Step "Step 1: Encrypting scripts"
    $protectScript = Join-Path $buildDir 'protect-gentle-vanguard.ps1'
    if (Test-Path $protectScript) {
        if ($DryRun) {
            Write-Warn "Dry run: would execute protect-gentle-vanguard.ps1"
        } else {
            try {
                & $protectScript
            }
            catch {
                throw "protect-gentle-vanguard.ps1 failed: $($_.Exception.Message)"
            }
        }
    } else {
        throw "protect-gentle-vanguard.ps1 not found in build/"
    }
} else {
    Write-Step "Step 1: Skipping encryption (-SkipEncrypt)"
}
#endregion

#region Step 2: Validate prerequisites
Write-Step "Step 2: Validating prerequisites"

$missing = @()
if (-not (Test-Path $protectedDir)) { $missing += "protected/ directory (run protect-gentle-vanguard.ps1 first)" }
if (-not (Test-Path (Join-Path $repoRoot 'keys\master.key'))) { $missing += "keys/master.key" }

$makeNsis = $null
$nsisDirs = @(
    'C:\Program Files (x86)\NSIS\makensis.exe',
    'C:\Program Files\NSIS\makensis.exe'
)
foreach ($dir in $nsisDirs) {
    if (Test-Path $dir) { $makeNsis = $dir; break }
}
if (-not $makeNsis) {
    $makeNsis = (Get-Command makensis.exe -ErrorAction SilentlyContinue)?.Source
}
if (-not $makeNsis) { $missing += "makensis.exe (install NSIS 3+)" }

if ($missing.Count -gt 0) {
    foreach ($m in $missing) { Write-Err $m }
    throw "Prerequisites missing. Install them and re-run."
}
Write-OK "makensis: $makeNsis"
Write-OK "protected/: exists"
Write-OK "master.key: exists"
#endregion

#region Step 3: Read version
Write-Step "Step 3: Reading version"

$versionFile = Join-Path $repoRoot 'VERSION'
$productVersion = if (Test-Path $versionFile) {
    (Get-Content $versionFile -Raw).Trim()
} else {
    Write-Warn "VERSION file not found, using 2.9.0"
    '2.9.0'
}
Write-OK "Version: $productVersion"
#endregion

#region Step 4: Compile launcher to .exe with ps2exe (if available)
Write-Step "Step 4: Compiling launcher"

$launcherPs1 = Join-Path $buildDir 'Gentle-Vanguard-Launcher.ps1'
$launcherExe = Join-Path $buildDir 'compiled\Gentle-Vanguard-Launcher.exe'

$ps2exe = Get-Command ps2exe -ErrorAction SilentlyContinue
if ($ps2exe -and (Test-Path $launcherPs1)) {
    if (-not $DryRun) {
        New-Item -ItemType Directory -Path (Split-Path $launcherExe -Parent) -Force | Out-Null
        & ps2exe -inputFile $launcherPs1 -outputFile $launcherExe -noConsole -requireAdmin
        if (Test-Path $launcherExe) {
            Write-OK "Compiled launcher: $launcherExe"
        } else {
            Write-Warn "ps2exe did not produce output, .nsi will use .ps1 launcher"
            $launcherExe = $null
        }
    }
} else {
    Write-Warn "ps2exe not available or launcher not found. Installer will use .ps1 wrapper."
    $launcherExe = $null
}
#endregion

#region Step 5: Generate .nsi dynamically
Write-Step "Step 5: Generating NSIS installer script"

$nsiPath = Join-Path $buildDir 'gentle-vanguard-installer-auto.nsi'
$outFile = Join-Path $distDir 'Gentle-Vanguard.exe'

if (-not $DryRun) {
    New-Item -ItemType Directory -Path $distDir -Force | Out-Null
}

$nsiContent = @"
; Gentle-Vanguard Installer v$productVersion — SINGLE FILE INSTALLER
; AUTO-GENERATED by create-installer.ps1 - do not edit manually

!define PRODUCT_NAME "Gentle-Vanguard"
!define PRODUCT_VERSION "$productVersion"
!define PRODUCT_PUBLISHER "Gentle-Vanguard"

SetCompressor lzma
RequestExecutionLevel admin

Name "`${PRODUCT_NAME} `${PRODUCT_VERSION}"
OutFile "$outFile"
InstallDir "`$PROGRAMFILES64\`${PRODUCT_NAME}"

!include "MUI2.nsh"

!insertmacro MUI_PAGE_WELCOME
!insertmacro MUI_PAGE_DIRECTORY
!insertmacro MUI_PAGE_INSTFILES
!insertmacro MUI_PAGE_FINISH

!insertmacro MUI_UNPAGE_CONFIRM
!insertmacro MUI_UNPAGE_INSTFILES

!insertmacro MUI_LANGUAGE "English"

Section "Core (Protected)" SecCore
  SectionIn RO

  SetOutPath "`$INSTDIR\protected"
  File /r "$protectedDir\*.*"

  SetOutPath "`$INSTDIR\public"
  File /r "$publicDir\*.*"
"@

if ($launcherExe -and (Test-Path $launcherExe)) {
    $nsiContent += @"

  SetOutPath "`$INSTDIR"
  File "$launcherExe"
"@
} else {
    # Generate a .bat wrapper file for the PowerShell launcher
    $batContent = "@echo off`r`npwsh -NoProfile -ExecutionPolicy Bypass -File `"%~dp0Gentle-Vanguard-Launcher.ps1`" %*`r`n"
    $batPath = Join-Path $buildDir 'Gentle-Vanguard.bat'
    if (-not $DryRun) {
        [System.IO.File]::WriteAllText($batPath, $batContent, [System.Text.Encoding]::ASCII)
    }

    $nsiContent += @"

  ; Launcher script (run via PowerShell)
  SetOutPath "`$INSTDIR"
  File "$launcherPs1"
  File "$batPath"
"@
}

$nsiContent += @"

  ; Keys directory + instructions
  CreateDirectory "`$INSTDIR\keys"
  FileOpen `$0 "`$INSTDIR\keys\HOW_TO_GET_KEY.txt" w
  FileWrite `$0 "GENTLE_VANGUARD MASTER KEY REQUIRED$\r$\n$\r$\n"
  FileWrite `$0 "This installation requires a master.key file to decrypt and run Gentle-Vanguard scripts.$\r$\n$\r$\n"
  FileWrite `$0 "To obtain the key:$\r$\n"
  FileWrite `$0 "1. Clone the private repository: gentle-vanguard$\r$\n"
  FileWrite `$0 "2. Copy keys/master.key to this directory: INSTDIR\keys\master.key$\r$\n$\r$\n"
  FileWrite `$0 "OR$\r$\n$\r$\n"
  FileWrite `$0 "Run the launcher and paste the key when prompted.$\r$\n"
  FileClose `$0

  ; Shortcuts
  CreateDirectory "`$SMPROGRAMS\`${PRODUCT_NAME}"
"@

if ($launcherExe -and (Test-Path $launcherExe)) {
    $shortcutBlock = @"

  CreateShortcut "`$SMPROGRAMS\`${PRODUCT_NAME}\`${PRODUCT_NAME}.lnk" "`$INSTDIR\Gentle-Vanguard-Launcher.exe"
  CreateShortcut "`$DESKTOP\`${PRODUCT_NAME}.lnk" "`$INSTDIR\Gentle-Vanguard-Launcher.exe"
"@
    $nsiContent += $shortcutBlock
} else {
    $shortcutBlock = @"

  CreateShortcut "`$SMPROGRAMS\`${PRODUCT_NAME}\`${PRODUCT_NAME}.lnk" "`$INSTDIR\Gentle-Vanguard.bat"
  CreateShortcut "`$DESKTOP\`${PRODUCT_NAME}.lnk" "`$INSTDIR\Gentle-Vanguard.bat"
"@
    $nsiContent += $shortcutBlock
}

$nsiContent += @"

  WriteUninstaller "`$INSTDIR\uninstall.exe"
SectionEnd

Section "Uninstall"
  RMDir /r "`$INSTDIR\protected"
  RMDir /r "`$INSTDIR\public"
  RMDir /r "`$INSTDIR\keys"
  Delete "`$INSTDIR\Gentle-Vanguard-Launcher.*"
  Delete "`$INSTDIR\Gentle-Vanguard.bat"
  Delete "`$INSTDIR\uninstall.exe"
  RMDir "`$INSTDIR"

  Delete "`$SMPROGRAMS\`${PRODUCT_NAME}\*.*"
  RMDir "`$SMPROGRAMS\`${PRODUCT_NAME}"
  Delete "`$DESKTOP\`${PRODUCT_NAME}.lnk"
SectionEnd
"@

if ($DryRun) {
    Write-Warn "Dry run: would generate .nsi at $nsiPath"
    Write-Host $nsiContent
} else {
    [System.IO.File]::WriteAllText($nsiPath, $nsiContent, [System.Text.UTF8Encoding]::new($false))
    Write-OK "Generated: $nsiPath"
}
#endregion

#region Step 6: Compile with NSIS
Write-Step "Step 6: Compiling installer"

if ($DryRun) {
    Write-Warn "Dry run: would compile with makensis"
} else {
    if (-not (Test-Path $distDir)) {
        New-Item -ItemType Directory -Path $distDir -Force | Out-Null
    }

    $result = & $makeNsis '/V2' $nsiPath 2>&1
    $exitCode = $LASTEXITCODE

    if ($exitCode -eq 0) {
        Write-OK "NSIS compilation successful"
        if (Test-Path $outFile) {
            $size = [math]::Round((Get-Item $outFile).Length / 1MB, 2)
            Write-OK "Output: $outFile ($size MB)"
        }
    } else {
        Write-Err "NSIS compilation failed (exit code $exitCode)"
        Write-Host $result
        throw "NSIS compilation failed"
    }
}
#endregion

Write-Step "Build complete!"
Write-Host ""
Write-Host "  NSIS script:  $nsiPath" -ForegroundColor Green
Write-Host "  Output:       $outFile" -ForegroundColor Green
Write-Host "  Version:      $productVersion" -ForegroundColor Green
if ($DryRun) {
    Write-Host "  [DRY RUN] No files were written" -ForegroundColor Yellow
}
